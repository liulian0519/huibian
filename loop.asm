DATA SEGMENT
	INF1 DB"please input a number(1-361):$"
	INF2 DB 0AH,0DH,"1+2+...$"
	INF3 DB 0AH,0DH,"=$"
	IBUF DB 6,0,6 DUP(?)
	OBUF DB  6 DUP(?)
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
	MOV DS,AX
	
	MOV DX,OFFSET INF1
	MOV AH,09H
	INT 21H

	MOV DX,OFFSET IBUF
	MOV AH,0AH
	INT 21H
	
	MOV BX,OFFSET IBUF+5
	MOV BYTE PTR [BX],'$'
	
	MOV DX,OFFSET INF2
	MOV AH,09H
	INT 21H
	MOV DX,OFFSET IBUF+2
	MOV AH,09H
	INT 21H
	
	MOV DX,OFFSET INF3
	MOV AH,09H
	INT 21H

	MOV CL,IBUF+1     
	MOV CH,0
	MOV SI,OFFSET IBUF+2
	MOV AX,0
AGAIN:  MOV DX,10
	MUL DX
	AND BYTE PTR[SI],0FH
	ADD AL,[SI]
	ADC AH,0
	INC SI
	LOOP AGAIN

	MOV CX,AX
	MOV AX,0
	MOV BX,1

	
LOOP2:  ADD AX,BX
	INC BX
	LOOP LOOP2
	
	
	MOV BX,OFFSET OBUF+5
	MOV BYTE PTR [BX],'$'
	MOV CX,10
LOOP1:  MOV DX,0
	DIV CX
	ADD DL,30H
	DEC BX
	MOV [BX],DL
	OR AX,AX
	JNZ LOOP1
	


	MOV DX,BX
	MOV AH,09H
	INT 21H	

	 
	MOV AH,4CH
	INT 21H
CODE ENDS
	END START

