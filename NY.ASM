DATA SEGMENT
     INFOR1 DB 0AH,0DH,"ARE YOU REALLY WANT TO EXIT?$"
     INFOR2 DB 0AH,0DH,"THANK YOU FOR YOUR USEING!$"
     INFOR3 DB 0AH,0DH,"LET'S CONTINUE!$"
     INFOR4 DB 0AH,0DH,"YOU PRESS AN ERROR KEY!$"
DATA ENDS
CODE SEGMENT
     ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
       MOV DS,AX
       MOV DX,OFFSET INFOR1
       MOV AH,09H
       INT 21H
       MOV AH,01H
       INT 21H
       CMP AL,'Y'
       JE  YES
       CMP AL,'y'
       JE  YES
       CMP AL,'N'
       JE NO
       CMP AL,'n'
       JE NO
       JMP ERROR
YES:  MOV DX,OFFSET INFOR2
      MOV AH,09H
      INT 21H
      JMP PEND
NO:   MOV DX,OFFSET INFOR3
      MOV AH,09H
      INT 21H
      JMP PEND
ERROR:MOV DX,OFFSET INFOR4
      MOV AH,09H
      INT 21H
PEND:MOV AH,4CH
      INT 21H
CODE ENDS
     END START
       
       
